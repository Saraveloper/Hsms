<h3>Step by Step Plan To Study In Canada </h3>
<p>The idea of studying in Canada is one to cherish, asides the availability of globally renowned schools and its affordability generally, the beautiful cultures and wonderful people in Canada make it a place you sure want to be. </p>
<p>One of the key steps that every Bangladeshi student needs to follow in order to study in Canada is – apply for a study permit. A Canadian study permit guarantees your Canadian residency permit. </p>
<p>Here are the steps to take in order to study in Canada… </p>
<h3> Step 1: Shortlist and apply </h3>
<p>Once you receive your SSC or HSC scores, you are free to start shortlisting universities that are of interest. You must choose up to 3 or 4 institutes. Ideally, the application process begins one year in advance. So, you can either wait for your academic results or start applying early on.</p>
<p>Before you start shortlisting and applying to institutes, it is advisable to take some standardized tests. Usually, you need to take IELTS when applying to Canadian schools. However, some institutes have distinct academic requirements. So, you also have to check the school’s website for entry requirements.</p>
<p>Upon receiving a letter of offer from your chosen university or college, you will need to apply for your study visa. At least 4–6 months prior to the commencement date of your program, you will have to file applications for your Canada study permit.</p>
<h3>Documents required for your study permit</h3>
<h4>To apply for your study permit, you will need:<h4>
	<p>An acceptance letter from your education institution, and</p>
	<p>A valid passport or travel document</p>
	<p>Proof that you can support yourself or any family members who might accompany you, while you’re in Canada</p>
<h3>Step 2: Apply for visa </h3>
<p>There are two ways to apply for a Canadian study visa from Bangladesh, either online or offline. If you desire to apply offline, you may need to visit the application center or the nearest embassy. You can also opt for the online process of applying for visa applications.</p>
<p>The visa fee could be paid either online or through bank transfer or at your nearest visa center. In addition, you will have to tender your biometrics to the center, if you apply through them.</p>
<p>However, you will have to attend the visa interview in-person and also take a medical exam in New Delhi. <p>
<p>students who wish to study in Canada need to fulfill certain requirements in order to secure a spot in a Canadian university. They are;<p>
<ul>
	<li>Valid passport</li>
	<li>Proof of acceptance letter</li>
	<li>Proof of funds</li>
	<li>Passport size photograph</li>
	<li>Immigration medical examination</li>
	<li>English language proficiency exam score</li>
	<li>Statement of purpose</li>
	<li>Credit card </li>

</ul>
<h3>Valid Passport </h3>
<p>You need a Valid Passport to be eligible to apply for a Study Permit. As far as the Canadian High Commission is concerned, it is essential that you have a passport which validity that extends to cover the intended stay in Canada. For instance, if you plan to travel to Canada in September 2022 for a two-year course, your passport will be valid until at least September 2024 </p>
<h3>Proof of acceptance letter</h3>
<p>You would need the acceptance letter from the university/institute you are planning to attend. This is because a Designated Learning Institute is the University that is acknowledged by the Immigration Department. 
Proof of funds
You have to provide evidence of funds at the time of submission for a study permit. This part of the requirement shows that you have enough money to sponsor your education and living expenses in Canada. Canadian immigration considers a student to have at least CAD 10,000 for each year of your stay. Furthermore, the student would also have to prove that he/she has enough funds for a return fair as well. </p>

<h3>Passport size photograph</h3>
<p>If are registering offline, you would need two passport-sized photographs that fit the given standards. For online application, you must obtain a digital copy of the photograph which should not be more than 4MB. Other specifications are standard about neutral facial expression, plain background as well as no headgear (unless for religious purposes). Note: <p>
<p>Size of the image should be at least 35 mm x 45 mm </p>
	<p>The image should be recent (not older than 6 months)</p>
<h3>Immigration medical examination</h3>
<p>Canadian Immigration requires international students from Bangladesh to undergo a compulsory Immigration Medical Examination from doctors. Students who wish to study in Canada from Bangladesh are to book an appointment and visit the listed doctors for a medical examination, preferably a week before they start their Visa Application. This is to give the doctor sufficient time to validate and upload the necessary documents. The panel physician will conduct a complete medical exam and may refer you for chest x-rays and laboratory tests. Once your exam has been performed, the physician will send the results to CIC. </p>
<h3>English language proficiency exam score </h3>
<p>Though not compulsory to have at the time of application, we strongly advise that you have your English Language Proficiency Score before you start your Visa Application Process. As it is, you would have had to submit your English language proficiency score to confirm your admission to some Canadian University. TOEFL, IELTS, etc. are all satisfactory. </p>
<h3>Statement of purpose</h3>
<p>As a student who wishes to study in Canada from Bangladesh, you would be expected to submit an essay stating the purpose of your trip to Canada and why you have chosen the particular school. This would be prompted as an optional document in the checklist but we strongly advise that you submit the same. <p>
<h3>Credit card </h3>
<p>While making an online application, you would also require a credit card to pay the application fee. The Visa Application Fee for Canada is CAD $160. Please note that the system only accepts credit cards and not debit cards. </p>


