                                  <h2>Which university should I go to?</h2>  
                                  <p>'I know what course I want to study. But where should I study it? Loads of places offer it.'</p> 

                                  <p>Once you’ve got your subject and course choice sorted, start looking around for where you want to be based for the next few years. As well as the specific course details above, here are a few pointers to get you thinking about where you want to go:
                                    Location
                                    For many, the big appeal of university is the opportunity to move out of home and experience living and studying in a new place. Check out our guide to choosing the right student city for you, including our quick quiz.
                                    </p> 


                                    <p>On the other hand, studying at university closer to home and commuting to classes might make more financial sense. A good start might be seeing what you'll get in student finance and other funding available, and then weighing that up against what your living costs could be.</p> 

                                    <p>Visit a few different university open days to get a feel for which might suit you best. </p> 

