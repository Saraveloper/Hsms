                    <p>'I don't know what to study at university.'</p>

                    <h5>If you're asking yourself this question, this is an important first step to get right. You don’t
                         want to waste your time and money on a subject you grow tired of, or end up dropping out of. </h5>
                         <p>To help you figure out what to study, ask yourself these three questions:</p>

                         <h5>Is it a subject you have already studied?
                        What are your favourite subjects? Which classes do you always look forward to or get top marks in? <br> <br>

                        For instance, if English literature was your favourite class at GCSE and A-level (or equivalent), and you read novels for pleasure in your own time, could an English degree be right for you?
                        </h5> <br> <br>
                                            
                        <h4>Consider the following:</h4> 
                        <p>
                        Will you still be interested in that subject for a further three or four years – enough to
                        motivate yourself to work and research independently?
                         </p>
                         <p>Any thoughts on life after university – what do you want to do and could your subject choice help with this? You may get to the end of this degree and still not know how you are going to earn a living. </p>

                            <p>On a positive note, many careers will consider graduates with a wide range of subjects.
 
                            Is it… a subject that relates to a career idea?
                            Perhaps you did work experience in an estate agency office, and are now considering a degree in real estate management in order to become a professional surveyor. Or maybe you’ve always wanted to be a journalist.
                            </p>

                            <p>How is the subject you’re considering viewed by the industry it is connected to? Do you need to take it to actually go into that career? For example, you don’t have to do a journalism degree to become a journalist – many degrees are considered.</p>
                            
                            <h6>Have you done any/enough work experience to see if this is the right career for you?</h6>

