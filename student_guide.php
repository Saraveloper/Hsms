                            </p>
                        
                            <p>Do you know what’s involved? Try speaking to a careers adviser, researching online or exploring in detail the type of modules you’ll study. Your perception of a subject (ie through what you've seen on TV) may be very different from reality.</p>

                                <p>What can you do later? While you may be really interested in a particular subject, keep in mind what your graduate prospects might look once you graduate. You never know, you might learn about jobs you never knew existed.</p>
                                
                                <h4>What course should I choose?</h4>
                                <p>'OK, I know the subject I want to study. But what course should I do?' </p> <br>

                                <p>
                                If you take a look at our subject guides, you'll find the full range of degree courses available for that degree subject. For example, if you're interested in history as a subject, you might choose to study a modern history course. <br>

                                Remember that a course might differ drastically from university to university, even if they have the same name. <br><br>

                                Also, you might find that the course you choose has to meet, or be accredited by, a professional body (eg civil engineering).
                                Have you actually read the outline of the course content provided by the university? <br><br>
                                 
                                Could you do any wider reading that relates to the course to prepare yourself? In any case, it would be good when writing your personal statement. <br> <br>                    

                                Watch now: This video has some tips on what to consider when deciding which course you want to study.  <br> <br> 



                                Type of assessment
                                Consider this: you’re doing a coursework-assessed Btec National in business studies, but the course you’re considering is mainly assessed by exams. There might be a big step change moving from college to university studies – could this pose a problem for you? <br> <br>

                                The Student Room forum member ihatePE recommended "looking into the content of the course [and] exploring how they assess you. Will you be writing long essays? Closed book exams? Multiple choice tests? I'm currently in civil engineering and there are no essays at all and I'm not a fan of drawing assessments of designing, which is one of the reasons I'm transferring." <br> <br>

                                You could think about: 
                                Which methods of assessment best suit you? Exams, coursework, practicals, group projects and presentations could all be in the mix in differing quantities.
                                 
                                Every university course will include a % breakdown of assessment as part of a new key information set it’s required to display on its website – have you checked it out yet?
                                Grade requirements
                                You have to identify five courses with entry requirements you have a good chance of meeting. Based on the universities that make you an offer, you’ll then have to decide which is going to be your firm (first) choice and your insurance (second) choice.
                                Are your five course choices sensibly spread, including a safe bet alongside a more ambitious option, based on what you’ve been predicted to achieve?
                                 
                                Does your insurance choice have lower entry requirements than your first choice? It should do! <br> <br>

                               <b> Also worth thinking about:</b>
                                Joint honours or interdisciplinary courses – can't decide between two, rather different subjects? You might be able to find a course that combines both. For instance, this could be a mixture of a subject you are already familiar with and something new. <br> 

                                The Student Room forum member wolfmoon88 shared that because they "have a lot of interests, I chose a course that is interdisciplinary which covers a lot of my interests in various fields".  <br> 
                                 
                                Scottish university courses – many Scottish universities let you apply for a named degree (ie politics), but you’ll cover a wide range of subjects in your first year. In the second year you can carry on with this initial subject, or specialise in some of the other subjects you tried out. <br> <br>

