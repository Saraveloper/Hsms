                <h3>Before applying for the Swedish student visa </h3>
                <p>There are a few conditions that you need to fulfil in order to be eligible for the student visa/residence permit. You can find them below:. </p>
                <ul>
                    <li>The university study programme you applied to must be full-time.</li>
                    <li>You must have a written confirmation that you have been admitted. List of universities recognised in Sweden.</li>
                    <li>You are recommended to pay the first tuition fee instalment (only if there is any tuition to be paid).</li>
                    <li>If you stay less than one year, you are required to have comprehensive health insurance.</li>
                    <li>Your passport should be valid for minimum 6 months</li>
                </ul>



