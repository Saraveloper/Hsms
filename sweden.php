                <h3>Why Sweden?</h3>
                <p> Sweden is a Scandinavian country in Northern Europe, bordered by Norway and Finland. It is the third largest country in the European Union by area, with a population of about 9.5 million. So what does Sweden have to offer an international student?. </p>
                <h3>Free Tuition for Students from EU Countries?</h3>
                <p>If you are an international student from a European Union country, you have another good reason to study in Sweden: tuition is free. Although the cost of living can be high in Sweden, it more than evens out when you factor in free tuition. With the rising costs of tuition in other countries, more and more students are applying to schools in Sweden. Last year, Lund saw 78 per cent increase in degree applications from the UK</p>
                <h3>Find a job after you graduate.?</h3>
                <p>A number of multinational firms are based in Sweden, and there are plenty of opportunities for ambitious grads – especially if you’re interested in becoming an engineer, software developer, architect, or any of these high-demand jobs listed by the Swedish Migration Agency (this document is in Swedish – use Google Translate if you’re not fluent yet!). </p>
                <p>IKEA, Spotify, Ericsson are all, unsurprisingly, Swedish. Innovation continues to be a Swedish specialty, as reflected in rankings such as the Global Innovation Index. Government investment in technology, climate change research and medicine and biotechnology has stimulated growth, research output and job opportunities</p>

                <h3>No Language Barrier </h3>
                <p>While the official language of Sweden is Swedish, a rich and beautiful language, in 2017 Swedes ranked second in the world for skills in English as a second language. As the world becomes increasingly connected and businesses seek to expand beyond national and linguistic boundaries, Sweden offers an excellent location to learn and develop your English language skills.
                However, a study abroad experience in Sweden would also be the best opportunity to learn some Swedish as well. Spoken by nearly ten million people, the Swedish language shares many similarities to English, and by knowing some, it will be that much easier to immerse into your new cultural surroundings. . </p>

