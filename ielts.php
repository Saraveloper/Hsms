	<h1>Everything you need to know about IELTS!</h1>
	<p>
		Applying to universities abroad is indeed an exhilarating experience—after all, the thought of living and studying in an entirely new country is enough to get anyone excited.
		<br><br>
		However, it can also be daunting and intimidating, especially given the sheer amount of paperwork you are required to submit. Chief among the many documents you will be asked for is a proof of English language proficiency, required when you are applying to study in a majority English speaking country.
		<br><br>
		For this purpose, standardized English language tests like the TOEFL or IELTS are administered routinely throughout the year across a number of locations in the world.
		<br><br>
		In this article, we will discuss the IELTS, its various types, and what type of IELTS is ideal for you as a grad school aspirant.
		<br><br>
		We will also touch upon the test format, scoring patterns, and the myriad advantages of having a high IELTS score.
	</p>
	<br><br>
	<h2>What exactly is the IELTS?</h2>

	<p>
		The International English Language Testing System, commonly abbreviated to IELTS, is one of the most globally recognized standardized English language proficiency tests with more than three million candidates every year.
		<br><br>
		Administered in partnership with the British Council, the test is required of those who are not native English speakers (those whose mother tongue is not English) but are desirous of working or studying abroad in majority English speaking countries like the UK, USA, Canada, Australia and New Zealand.
		<br><br>
		The candidate is examined across four areas of language—speaking, listening, reading and writing—and based on his or her proficiency, is given a score on a 0-9 scale for each component. The overall band score will be awarded based on an average of scores from all the four components.
		<br><br>
		Universities seeking an IELTS score clearly stipulate their IELTS score requirements on the official website so you can know exactly what you are aiming at. Usually, score requirements are upwards of 6.5 (overall), although of course it can vary from university to university.
	</p>

	<p>The test is administered four times a month, across nearly ninety locations in India, so you don’t have to wait too long before you take your test. Once available, the test results are valid for two years.</p>
	<br><br>
	<h2>What are the different types of IELTS tests?</h2> 
	<p>
		Because the IELTS is used for both education and immigration purposes, there are two different modules of the test— the <i>Academic</i> and the <i>General</i>.
	</p><br>
	<ul class="points">
		<li>
			The <b>Academic</b> is designed for students aspiring to pursue their higher education in an English-speaking country. As such, the test intends to gauge your ability to grasp and understand academic language, and requires of you a considerable command over the English language. It is also aimed at professionals such as doctors or nurses who want to set up their own practice in an English-speaking country.
		</li><br>

		<li>
			The <b>General</b> module is undertaken by those wishing to obtain non-academic training, or to gain work experience. It is also taken for immigration purposes. The General module tests conversational skills, and ability to comfortably converse in English in general social or business situations.
		</li> <br>
	</ul>
	<br>	
	<h2>
		What is different about the two modules?
	</h2>

	<p>
		The IELTS General and Academic test takers have the same Listening and Speaking sections. What is different, therefore,  is the Reading and Writing components. Let us take a look at some of these differences.
	</p><br>
	<p>	
		<b>Writing:</b> The most discernible differences between the academic and general IELTS are manifest in the writing section. For those opting for the Academic module, the first writing task (of about 150-200 words) will require the candidate to succinctly describe, summarise or explain a chart, graph, table or diagram. The task is designed to gauge the candidate’s ability to choose important features from less important ones, to accurately report factual information, and to describe and compare data.
		<br><br>
		In the General module, the first writing task is concerned with letter writing. Candidates are presented with a problem or situation (such as applying for a job, or asking for time off work), to which they are expected to write a letter that expresses an opinion, engages the reader in a conversation, and also provides factual information.
		<br><br>
		The second task of the Writing component is common to both modules—candidates will be given a brief opinion or statement, based off which they are required to produce an extended piece of writing in response to it, based on their own personal knowledge and experience. Usually, the statement given to General Module students are easier to attempt.
	</p>
	<br>
	<p>
		<b>Reading:</b> The Reading section of the IELTS tests the candidate’s ability to understand the main idea of the text, understanding details and nuances, and his ability to synthesize and reproduce the knowledge gleaned from the texts. Although the Reading component in both modules consists of three tasks, the Academic module entails longer passages, with texts ranging from descriptive and factual to discursive and analytical, and could also include non-verbal material such as graphs, charts and diagrams. On the other hand, section 1 of the General Module Reading component contains two or three short factual texts, section 2 contains two short factual texts relating to work, while the third section has one longer text on a topic of general interest.
	</p>
	<br><br>
	<h2>
		Which IELTS test should I choose?
	</h2>
	<p>As an MS aspirant, you will be required to take the <b>Academic IELTS</b>, which measures your English language proficiency, and gauges whether you will be comfortable in an academic, higher learning environment which demands a robust command over the English language.</p>
	<br><br>

	<h2>What is the test format for the IELTS Academic?</h2>
	<p>The exam is conducted over 2 hours and 45 minutes, and consists of three sections, with the Speaking component conducted separately (lasting 11-14 minutes).</p>

	
	<p>
		<b>Listening:</b> The listening section consists of 40 questions to be answered over 30 minutes (plus ten more minutes to transfer your answers into the sheet). The candidate will listen to four different recordings based off which they will be required to answer the questions asked. The first recording is a conversation between two people about everyday situations, while the second one is a monologue about some facet of everyday social contexts. The third recording takes place in an academic setting with up to four people, and the fourth one is again a monologue, this time on a matter of academic interest (such as a lecture). Each question carries one mark, although poor spelling and grammar is liable to be penalised. This component gauges your ability to understand and respond to spoken English, and also how well you are able to grasp and understand factual information and the opinions and ideas of the speakers.
	</p>
	<br>
	<p>
		<b>Reading:</b> The duration of this component is 60 minutes, with 40 questions worth one mark each in total. You are required to read three long passages, based on which a number of questions will be asked. The types of questions include, but are not limited to, multiple choice, identifying the writer’s intentions, opinions and claims, short answer writing, and sentence completion. The texts are taken from a wide range of magazines, journals, newspapers, books and other texts. The intention of the Reading section is to test your ability to grasp the main underlying ideas and opinions of the text, as well as to read for details, understand the arguments being made, and also to skim-read when there is a paucity of time.
	</p>
	<br>
	<p>
		<b>Writing:</b> Again, the duration of the Writing section is 60 minutes, within which time you will have to complete two writing tasks. In Task 1, you will be given some form of visual material (graphs, charts, diagrams, tables, etc), and based on your understanding of the data, you will be required to summarise the information in your own words. You will not be allowed to use bullet points or notes—the answer must be in paragraphs. The use of bullet points will lead to penalization, as will writing too short an answer—your answer must be at least 150 words. The answer must be based on the data only—writing irrelevant, off-topic responses will also be penalized. Ideally, you should not spend more than 20 minutes on this task. <br><br>
		For the second task, you will be given an opinion or idea, using which as a base you must write an extended response. This task contributes twice as much to the band score as task one, so it is crucial that you give yourself enough time for this task. You will be required to write at least 250 words, failing which you will be risking your chance at a good score. The answer must be nuanced and opinionated—for instance, if the question is about a specific aspect of air pollution, you must not write on air pollution in general, but about what you feel about this particular aspect. This task measures your ability to form opinions and convey them, to write clearly and succinctly, and to form a balanced, well-informed argument. You should not spend more than 40 minutes on this task. Plagiarism, not meeting the word limit, and writing answers in bullets will lead to severe penalisation.
	</p>
	<br>
	<p>
		<b>Speaking:</b> Designed to test your ability to speak and communicate clearly and articulately, the speaking component is essentially an interview between the candidate and the examiner, and is spread across three parts. In part 1, lasting for 4-5 minutes, the examiner introduces himself, and asks the candidate basic question about himself, his family, friends, occupation and hobbies. In Part 2, the candidate will be given a card which asks him to speak at length about a specific topic, and lasts for 2-3 minutes. In part 3, lasting 4-5 minutes, the examiner and candidate further discuss the topic given in part 2.
	</p>
	<br><br>
	<h2>Scoring Pattern</h2>

	<p>
		Speaking and Writing are entirely subjective and thus scores are given accordingly, based on ability to articulate clearly and precisely, as well as usage of vocabulary, grammar, syntax and sentence structure. Accordingly, a score between 0 (non-user) to 9 (expert user) is awarded.
		<br><br>
		In Reading and Listening, there are 40 questions carrying 1 mark each. For every correct answer you get a single mark, which then cumulates into the final raw scores that you have. For example— if 30 of your answers are correct in listening, then 30 will be your raw score in IELTS listening out of 40, which will then be converted into bands.
		<br><br>
		In order to convert scores to bands, the raw score is matched with the corresponding band score. So if your raw score is 30, it will be converted into 7 bands. Each section has its own band score, and based on the average of the four bands, a score is given.
	</p>
	<br><br>
	<h2>What is a good IELTS score for me?</h2>
	<p>
		As we’ve seen, universities generally stipulate their band score requirements based on programs of study on their website. However, for MS aspirants, a score between 6 (competent user) to 8 (almost expert user) is considered an excellent score. Of course, the perfect 9 score is an extremely elusive one!
	</p>


	<p>
		Now, the IELTS may seem like a cake walk, but the truth is that students often arrive at the exam centre and are taken aback by the test, not because their command over English is weak, but due to lack of acquaintance with the test format and scoring. Scoring high on the exam is actually surprisingly easy if you practice consistently, and work on your reading and writing skills.
	</p>



			


         
